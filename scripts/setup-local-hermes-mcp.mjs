#!/usr/bin/env node
/**
 * Configure local Hermes (rise-path profile) + Rise Path MCP connection.
 *
 * Default: SSE — run MCP separately (npm run mcp:local), Hermes connects to :3100.
 * Optional: --stdio — Hermes spawns MCP as subprocess (no npm run mcp:local).
 *
 * Usage:
 *   node scripts/setup-local-hermes-mcp.mjs
 *   node scripts/setup-local-hermes-mcp.mjs --stdio
 */

import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';
import { randomBytes } from 'crypto';
import { spawnSync } from 'child_process';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const ENV_PATH = path.join(ROOT, '.env.local');
const HERMES_PROFILE = path.join(
  process.env.HOME || '',
  '.hermes/profiles/rise-path',
);

const useStdio = process.argv.includes('--stdio');

async function readEnvFile(filePath) {
  const env = {};
  try {
    const raw = await fs.readFile(filePath, 'utf8');
    for (const line of raw.split(/\r?\n/)) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#')) continue;
      const idx = trimmed.indexOf('=');
      if (idx < 1) continue;
      env[trimmed.slice(0, idx).trim()] = trimmed.slice(idx + 1).trim().replace(/^["']|["']$/g, '');
    }
  } catch {
    return null;
  }
  return env;
}

async function resolveOpenRouterKey(env) {
  if (env.OPENROUTER_API_KEY?.trim()) return env.OPENROUTER_API_KEY.trim();
  const globalEnv = await readEnvFile(path.join(process.env.HOME || '', '.hermes/.env'));
  if (globalEnv?.OPENROUTER_API_KEY?.trim()) return globalEnv.OPENROUTER_API_KEY.trim();
  return '';
}

async function upsertEnvKey(filePath, key, value) {
  let raw = '';
  try {
    raw = await fs.readFile(filePath, 'utf8');
  } catch {
    raw = await fs.readFile(path.join(ROOT, 'env.local.template'), 'utf8');
  }

  const line = `${key}=${value}`;
  const pattern = new RegExp(`^${key}=.*$`, 'm');
  const next = pattern.test(raw)
    ? raw.replace(pattern, line)
    : `${raw.trimEnd()}\n${line}\n`;
  await fs.writeFile(filePath, next.endsWith('\n') ? next : `${next}\n`, 'utf8');
}

function ensureHermesProfile() {
  const result = spawnSync('hermes', ['profile', 'list'], { encoding: 'utf8' });
  if (result.status !== 0) {
    console.error('❌ Hermes CLI not found. Install: https://hermes-agent.nousresearch.com/');
    process.exit(1);
  }
  if (!result.stdout.includes('rise-path')) {
    console.log('==> Creating Hermes profile: rise-path');
    const created = spawnSync('hermes', ['profile', 'create', 'rise-path'], { encoding: 'utf8' });
    if (created.status !== 0) {
      console.error(created.stderr || created.stdout);
      process.exit(1);
    }
  }
}

async function main() {
  console.log('# Rise Path local Hermes + MCP setup\n');

  const env = await readEnvFile(ENV_PATH);
  if (!env) {
    console.error('❌ .env.local not found. Run: cp env.local.template .env.local');
    process.exit(1);
  }

  if (!env.DATABASE_URL_PHASE1) {
    console.error('❌ DATABASE_URL_PHASE1 missing in .env.local');
    process.exit(1);
  }

  let bridgeToken = env.RISE_PATH_BRIDGE_TOKEN?.trim();
  if (!bridgeToken || bridgeToken === 'change-me-bridge-token') {
    bridgeToken = randomBytes(24).toString('hex');
    await upsertEnvKey(ENV_PATH, 'RISE_PATH_BRIDGE_TOKEN', bridgeToken);
    console.log('✅ Generated RISE_PATH_BRIDGE_TOKEN in .env.local');
  }

  const hermesKey = env.HERMES_API_KEY?.trim() || 'change-me-local-dev';
  const apiServerKey = hermesKey;
  if (!env.HERMES_API_URL) {
    await upsertEnvKey(ENV_PATH, 'HERMES_API_URL', 'http://127.0.0.1:8642');
  }
  if (!env.HERMES_API_KEY) {
    await upsertEnvKey(ENV_PATH, 'HERMES_API_KEY', hermesKey);
  }

  ensureHermesProfile();
  await fs.mkdir(HERMES_PROFILE, { recursive: true });

  const templateName = useStdio ? 'config.local-stdio.yaml' : 'config.local-sse.yaml';
  let config = await fs.readFile(path.join(ROOT, 'hermes', templateName), 'utf8');
  config = config
    .replaceAll('RISE_PATH_ROOT', ROOT)
    .replaceAll('RISE_PATH_SKILLS_DIR', path.join(ROOT, 'skills'))
    .replaceAll('__RISE_PATH_BRIDGE_TOKEN__', bridgeToken);

  await fs.writeFile(path.join(HERMES_PROFILE, 'config.yaml'), config, 'utf8');

  const openRouterKey = await resolveOpenRouterKey(env);
  if (openRouterKey && !env.OPENROUTER_API_KEY) {
    await upsertEnvKey(ENV_PATH, 'OPENROUTER_API_KEY', openRouterKey);
    console.log('✅ Copied OPENROUTER_API_KEY from ~/.hermes/.env → .env.local');
  }

  const hermesEnv = [
    '# Generated by scripts/setup-local-hermes-mcp.mjs',
    'API_SERVER_ENABLED=true',
    'API_SERVER_HOST=127.0.0.1',
    `API_SERVER_PORT=${env.API_SERVER_PORT || '8642'}`,
    `API_SERVER_KEY=${apiServerKey}`,
    `HERMES_API_KEY=${apiServerKey}`,
    `RISE_PATH_BRIDGE_TOKEN=${bridgeToken}`,
    `DATABASE_URL_PHASE1=${env.DATABASE_URL_PHASE1}`,
    `PHASE1_USER_ID=${env.PHASE1_USER_ID || '00000000-0000-0000-0000-000000000001'}`,
    openRouterKey ? `OPENROUTER_API_KEY=${openRouterKey}` : '# OPENROUTER_API_KEY=set-in-hermes-model-or-here',
    '',
  ].join('\n');
  await fs.writeFile(path.join(HERMES_PROFILE, '.env'), hermesEnv, 'utf8');

  console.log(`✅ Hermes profile: ${HERMES_PROFILE}`);
  console.log(`   transport: ${useStdio ? 'stdio (MCP subprocess)' : 'SSE → http://127.0.0.1:3100'}`);
  console.log(`   skills: ${path.join(ROOT, 'skills')}`);
  console.log('');
  console.log('Next:');
  if (!useStdio) {
    console.log('  Terminal 1: npm run mcp:local');
    console.log('  Terminal 2: npm run hermes:local');
  } else {
    console.log('  Terminal 1: npm run dev');
    console.log('  Terminal 2: npm run hermes:local');
  }
  console.log('  Verify:     npm run smoke:hermes-local');
  if (!openRouterKey) {
    console.log('\n⚠️  OPENROUTER_API_KEY not found — run: hermes -p rise-path model');
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});